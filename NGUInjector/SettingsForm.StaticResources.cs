        private void ApplyStaticResources(object target, string objectName)
        {
            if (target == null || !StaticResourceMap.TryGetValue(objectName, out var resources))
                return;

            foreach (var resource in resources)
            {
                if (resource.PropertyName == "Items")
                {
                    AddStaticItem(target, resource.Value);
                    continue;
                }

                if (resource.PropertyName == "LayoutSettings" && target is TableLayoutPanel panel && resource.Value is string xml)
                {
                    ApplyTableLayoutStyles(panel, xml);
                    continue;
                }

                var property = target.GetType().GetProperty(resource.PropertyName, BindingFlags.Instance | BindingFlags.Public);
                if (property == null || !property.CanWrite)
                    continue;

                property.SetValue(target, resource.Value, null);
            }
        }

        private static void AddStaticItem(object target, object value)
        {
            if (target is ComboBox comboBox)
                comboBox.Items.Add(value);
            else if (target is ListBox listBox)
                listBox.Items.Add(value);
        }

        private static void ApplyTableLayoutStyles(TableLayoutPanel panel, string xml)
        {
            if (string.IsNullOrWhiteSpace(xml))
                return;

            var columns = ExtractLayoutStyles(xml, "Columns");
            var rows = ExtractLayoutStyles(xml, "Rows");

            if (!string.IsNullOrEmpty(columns))
            {
                panel.ColumnStyles.Clear();
                foreach (var style in ParseLayoutStyles(columns))
                    panel.ColumnStyles.Add(new ColumnStyle(style.Type, style.Size));
            }

            if (!string.IsNullOrEmpty(rows))
            {
                panel.RowStyles.Clear();
                foreach (var style in ParseLayoutStyles(rows))
                    panel.RowStyles.Add(new RowStyle(style.Type, style.Size));
            }
        }

        private static IEnumerable<LayoutStyle> ParseLayoutStyles(string styles)
        {
            var parts = styles.Split(new[] { ',' }, StringSplitOptions.RemoveEmptyEntries);
            for (var i = 0; i < parts.Length;)
            {
                var typeText = parts[i].Trim();
                if (!TryParseSizeType(typeText, out var type))
                {
                    i++;
                    continue;
                }

                i++;
                var sizeText = i < parts.Length ? parts[i].Trim() : "0";
                i++;

                if (i < parts.Length && !TryParseSizeType(parts[i].Trim(), out _) && IsNumberPart(sizeText) && IsNumberPart(parts[i].Trim()))
                {
                    sizeText = $"{sizeText}.{parts[i].Trim()}";
                    i++;
                }

                if (!float.TryParse(sizeText, NumberStyles.Float, CultureInfo.InvariantCulture, out var size))
                    size = 0f;

                yield return new LayoutStyle(type, size);
            }
        }

        private static bool TryParseSizeType(string value, out SizeType sizeType)
        {
            try
            {
                sizeType = (SizeType)Enum.Parse(typeof(SizeType), value, false);
                return true;
            }
            catch
            {
                sizeType = SizeType.AutoSize;
                return false;
            }
        }

        private static bool IsNumberPart(string value)
        {
            if (string.IsNullOrEmpty(value))
                return false;

            foreach (var c in value)
            {
                if (!char.IsDigit(c) && c != '-' && c != '+')
                    return false;
            }

            return true;
        }

        private static string ExtractLayoutStyles(string xml, string elementName)
        {
            var elementStart = $"<{elementName}";
            var elementIndex = xml.IndexOf(elementStart, StringComparison.Ordinal);
            if (elementIndex < 0)
                return null;

            var attributeStart = "Styles=\"";
            var attributeIndex = xml.IndexOf(attributeStart, elementIndex, StringComparison.Ordinal);
            if (attributeIndex < 0)
                return null;

            attributeIndex += attributeStart.Length;
            var attributeEnd = xml.IndexOf("\"", attributeIndex, StringComparison.Ordinal);
            return attributeEnd < 0 ? null : xml.Substring(attributeIndex, attributeEnd - attributeIndex);
        }
